def get_quiz_questions(
    domain: Optional[str] = None,
    count: int = 10,
) -> List[dict]:
    """Return *count* random questions, optionally filtered by domain."""
    pool = [q for q in QUESTIONS if domain is None or q["domain"] == domain]
    if not pool:
        pool = list(QUESTIONS)
    count = min(count, len(pool))
    selected = random.sample(pool, count)
    # Strip answer from the returned payload (client validates separately)
    return [
        {
            "id": q["id"],
            "domain": q["domain"],
            "question": q["question"],
            "choices": q["choices"],
            "answer": q["answer"],
            "comment": q["comment"],
        }
        for q in selected
    ]
