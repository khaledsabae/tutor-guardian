def build_unit(slug, domain, age, behavior, severity, intervention,
               ref_type, ref_info, title, text_simplified, labels):
    return {
        "id": f"seed-{slug}",
        "domain": domain,
        "source_file": f"curated/{ref_info}",
        "title": title,
        "language": "ar",
        "age_group": age,
        "behavior_type": behavior,
        "intervention_type": intervention,
        "severity": severity,
        "reference_type": ref_type,
        "reference_info": ref_info,
        "text_original": text_simplified,
        "text_simplified": text_simplified,
        "labels": labels,
        "created_at": NOW,
        "updated_at": NOW,
        "version": "1.0.0",
        "source_meta": {"source_title": ref_info, "source_author": ""},
    }


def main():
    written = 0
    for row in UNITS:
        unit = build_unit(*row)
        (UNITS_DIR / f"{unit['id']}.json").write_text(
            json.dumps(unit, ensure_ascii=False, indent=2), encoding="utf-8")
        written += 1
    print(f"Wrote {written} curated units (batch 2) to {UNITS_DIR}")


if __name__ == "__main__":
    main()
