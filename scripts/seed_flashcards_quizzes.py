def main():
    # Merge supplements into DATA (→ 5 cards + 3 quiz Qs each).
    for lid, extra in SUPP.items():
        if lid in DATA:
            DATA[lid]["fc"] = DATA[lid]["fc"] + extra["fc"]
            DATA[lid]["qz"] = DATA[lid]["qz"] + extra["qz"]
    FC_DIR.mkdir(parents=True, exist_ok=True)
    QZ_DIR.mkdir(parents=True, exist_ok=True)
    index = json.loads(INDEX.read_text(encoding="utf-8"))
    by_id = {l["lesson_id"]: l for l in index["lessons"]}
    titles = {f.stem: json.loads(f.read_text(encoding="utf-8"))["title"]
              for f in LESSONS_DIR.glob("*.json")}
    done = 0
    for lid, payload in DATA.items():
        title = titles.get(lid, lid)
        cards = [{"front": f, "back": b} for f, b in payload["fc"]]
        quiz = payload["qz"]
        fc_id, qz_id = f"gen_{lid}_fc", f"gen_{lid}_qz"
        fc_rel = f"docs/lesson_assets/flashcards/{fc_id}.json"
        qz_rel = f"docs/lesson_assets/quizzes/{qz_id}.json"
        (FC_DIR / f"{fc_id}.json").write_text(json.dumps(
            {"title": f"{title} — بطاقات", "cards": cards},
            ensure_ascii=False, indent=2), encoding="utf-8")
        (QZ_DIR / f"{qz_id}.json").write_text(json.dumps(
            {"title": f"{title} — اختبار", "questions": quiz},
            ensure_ascii=False, indent=2), encoding="utf-8")
        entry = by_id.get(lid)
        if entry is not None:
            a = entry.setdefault("assets", {})
            a["flashcards"] = [{"id": fc_id, "file": fc_rel,
                                "title": f"{title} — بطاقات", "item_count": len(cards)}]
            a["quizzes"] = [{"id": qz_id, "file": qz_rel,
                             "title": f"{title} — اختبار", "item_count": len(quiz)}]
        done += 1
    INDEX.write_text(json.dumps(index, ensure_ascii=False, indent=2),
                     encoding="utf-8")
    print(f"Wrote flashcards+quizzes for {done}/{len(DATA)} lessons")


if __name__ == "__main__":
    main()
