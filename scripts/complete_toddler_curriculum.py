FC_DIR = ROOT / "docs/lesson_assets/flashcards"


def _clean(text: str) -> str:
    return re.sub(r"\s+", " ", (text or "").replace("\n", " ")).strip()


def body_from_flashcards(lid: str, title: str) -> dict:
    """Assemble the lesson body from the already-sourced flashcards for
    this lesson (NotebookLM-generated from the user's PDFs). Pure
    restructuring of existing sourced content — nothing is authored."""
    fc_path = FC_DIR / f"gen_{lid}_fc.json"
    cards = []
    if fc_path.exists():
        cards = json.loads(fc_path.read_text()).get("cards", [])
    if not cards:
        return {
            "summary": f"يتناول هذا الدرس {title} للأطفال من سنتين إلى ثلاث سنوات. "
                       "افتح البطاقات التعليمية والاختبار بالأعلى للمحتوى التفصيلي.",
            "try_this": "طبّق ما ورد في البطاقات التعليمية خطوة بخطوة هذا الأسبوع.",
            "reflection_prompts": ["ما أكثر ما ينطبق على طفلك مما قرأت؟"],
        }
    backs = [_clean(c.get("back", "")) for c in cards if c.get("back")]
    fronts = [_clean(c.get("front", "")) for c in cards if c.get("front")]
    summary = f"يتناول هذا الدرس «{title}» للأطفال من سنتين إلى ثلاث سنوات. "
    summary += " ".join(backs[:3])
    return {
        "summary": summary[:900],
        # the most actionable card's advice
        "try_this": "جرّب هذا الأسبوع: " + (backs[0] if backs else ""),
        "reflection_prompts": fronts[:2],
    }


def find_units(domain: str, n: int = 2) -> list[str]:
    """Borrow a couple of prenatal-1/2-3 unit ids of the same domain for the
    lesson's unit_ids (used only for the KB-reference chip)."""
    import glob
    ids = []
    for p in glob.glob(str(ROOT / "knowledge_base/units/*.json")):
        d = json.loads(Path(p).read_text())
        if d.get("domain") == domain and d.get("age_group") in ("prenatal-1", "2-3", "unspecified"):
            ids.append(d.get("id") or Path(p).stem)
        if len(ids) >= n:
            break
    return ids


