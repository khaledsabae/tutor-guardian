def build_flashcards(spec):
    return {
        "title": spec["fc_title"],
        "cards": [{"front": f, "back": b} for f, b in spec["cards"]],
    }


def build_quiz(spec):
    questions = []
    for q in spec["quiz"]:
        text, opts, hint = q
        questions.append({
            "question": text,
            "answerOptions": [
                {"text": t, "isCorrect": c, "rationale": r} for t, c, r in opts
            ],
            "hint": hint,
        })
    return {"title": spec["qz_title"], "questions": questions}


def assert_clean(obj, where):
    m = CJK_CYR.findall(json.dumps(obj, ensure_ascii=False))
    if m:
        sys.exit(f"PURITY FAIL in {where}: {m[:5]}")


def main():
    idx = json.loads(INDEX.read_text(encoding="utf-8"))
    by_short = {l["lesson_id"]: l for l in idx["lessons"]}
    wired = 0
    for short_id, spec in CONTENT.items():
        if short_id not in by_short:
            sys.exit(f"unknown lesson_id: {short_id}")
        fc = build_flashcards(spec)
        qz = build_quiz(spec)
        assert_clean(fc, f"{short_id} flashcards")
        assert_clean(qz, f"{short_id} quiz")

        fc_id = f"gen_{short_id}_fc"
        qz_id = f"gen_{short_id}_qz"
        fc_rel = f"docs/lesson_assets/flashcards/{fc_id}.json"
        qz_rel = f"docs/lesson_assets/quizzes/{qz_id}.json"
        (ROOT / fc_rel).write_text(json.dumps(fc, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        (ROOT / qz_rel).write_text(json.dumps(qz, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

        lesson = by_short[short_id]
        assets = lesson.setdefault("assets", {})
        assets["flashcards"] = [{"id": fc_id, "file": fc_rel, "title": fc["title"], "item_count": len(fc["cards"])}]
        assets["quizzes"] = [{"id": qz_id, "file": qz_rel, "title": qz["title"], "item_count": len(qz["questions"])}]
        wired += 1
        print(f"  wired {short_id}: {len(fc['cards'])} cards + {len(qz['questions'])} questions")

    INDEX.write_text(json.dumps(idx, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"done — {wired} lessons wired with clean assets")


if __name__ == "__main__":
    main()
